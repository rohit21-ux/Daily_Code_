'''
# Comparison Operators in Python
operatr              Name                      Example
 ==                  Equal                      a == b
 !=                  Not Equal                  a!=b
 >                   Greater Than               a > b
 <                   Less Than                  a < b
 >=                  Greater than or equal to   a>= b              
                    
 <=                 Less than or equal to       a <= b
'''

print("Comparison operators in python \n")

a = 10
b = 20

# Equal
print("a == b:", a==b)  #false

# Not Equal
print("a != b:", a!=b)  #true

#Greater Than
print("a>b:",a>b)  #False

#Less Than
print("a<b:",a<b)  #True

#Greater Than or Equal to
print("a>=b:", a>=b)  #false

#Less Than or Equal to
print("a<=b:", a<=b)  #True

